    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Pet p = new Pet();
        try {
            PetDao pd = new PetDao();
            p.setAnoNascimentoPet(Integer.parseInt(txtAnoNascimento.getText()));
            p.setCorPeloPet(txtCorPelo.getText());
            p.setNomePet(txtNomePet.getText());
            p.setRacaPet(txtRaca.getText());
            if ((Masculino.isSelected())) {
                p.setSexoPet("M");
            } else {
                p.setSexoPet("F");
            }
            if (p.validarNome(txtNomePet.getText())) {
              pd.create(p);
              
            }else{
                JOptionPane.showMessageDialog(null,"Nome Invalido 3 ou mais caracteres");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

